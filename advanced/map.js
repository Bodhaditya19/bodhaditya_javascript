/**
 * Map is a collection of elements where each element is stored as a 
 * Key, value pair. Map object can hold both objects and primitive values 
 * as either key or value. When we iterate over the map object it 
 * returns the key,value pair in the same order as inserted
 */

let myMap = new Map([
    ['firstname','Bodhaditya'], ['lastname', 'Mukherjee'], ['Age','27']
]);  
console.log(myMap);

let newMap = [ ['firstname','Bodhaditya'], ['lastname', 'Mukherjee'], ['Age','27'] ] ;
console.log(newMap);